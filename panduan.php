<ul> ► Tidur cukup </ul>
<ul> ► Olahraga teratur </ul>
<ul> ► Hindari rokok dan alkohol </ul>  
<ul> ► Manajemen stress  </ul>
<ul> ► Melakukan vaksinasi </ul><br>

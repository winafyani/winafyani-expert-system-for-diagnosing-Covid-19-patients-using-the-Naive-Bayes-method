<ul> ► Sesak ketika beraktivitas ringan dan tidak membaik dengan  posisi apa pun. </ul>
<ul> ► Perhatian, ingatan, dan pemikiran Anda tidak membaik, sehingga  sangat sulit bagi Anda untuk melakukan aktivitas sehari-hari,  atau menghentikan anda untuk kembali bekerja. </ul>
<ul> ► Suasana hati anda memburuk, stress, depresi terutama jika  berlangsung selama beberapa minggu. </ul><br>
<ul> 8. Setelah sembuh,apa boleh divaksin ? </ul>

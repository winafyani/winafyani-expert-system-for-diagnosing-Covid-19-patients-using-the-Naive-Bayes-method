</li><br>
<ul style="color: black;"><u>Pembagian Status Pasien Berdasarkan Gejala yang Dialami </u></ul>
<li style="color: black;">
    A. Pasien Tanpa Gejala </li>
    <li style="color: black;">Kondisi ini merupakan kondisi teringan. Pasien tidak ditemukan gejala.   
</li><br>
<li style="color: black;">
    B. Ringan/tidak berkomplikasi</li>
<li style="color: black;">Pasien dengan infeksi saluran napas oleh virus tidak berkomplikasi dengan gejala tidak spesifik seperti demam, lemah, batuk 
(dengan atau tanpa produksi sputum),anoreksia, malaise, nyeri otot, sakit tenggorokan, sesak ringan, kongesti hidung, sakit kepala.
Meskipun jarang, pasien dapat dengan keluhan diare, mual atau muntah. Pasien usia tua dan immunocompromised gejala atipikal.</li><br>
<li style="color: black;"> C. Sedang / Moderat</li>
<li style="color: black;">
    Pasien remaja atau dewasa dengan pneumonia tetapi tidak ada tanda pneumonia berat dan tidak membutuhkan suplementasi
oksigen Atau Anak-anak dengan pneumonia tidak berat dengan keluhan batuk atau sulit bernapas disertai napas cepat. 
</li><br>
<li style="color: black;"> D. Berat /Pneumonia Berat </li>
<li style="color: black;"> Pasien remaja atau dewasa dengan demam atau dalam pengawasan infeksi saluran napas/pneumonia, ditambah satu dari: frekuensi
napas > 30 x/menit, distress pernapasan berat, atau saturasi oksigen (SpO2) <93% pada udara kamar atau rasio PaO2/FiO2 < 300. Atau Pasien anak dengan batuk atau kesulitan bernapas, ditambah setidaknya satu dari berikut ini:
<li style="color: black;"> * sianosis sentral atau SpO2 <90%;</li>
<li style="color: black;"> * distres pernapasan berat (seperti mendengkur, tarikan dinding dada yang berat);</li>
<li style="color: black;"> * tanda pneumonia berat: ketidakmampuan menyusui atau minum, letargi atau penurunan kesadaran, atau kejang.</li><br>
<li style="color: black;"> E. Kritis</li>
<li style="color: black;"> Pasien dengan gagal napas, Acute Respiratory Distress Syndrome (ARDS), syok sepsis dan/atau multiple organ failure</li><br>


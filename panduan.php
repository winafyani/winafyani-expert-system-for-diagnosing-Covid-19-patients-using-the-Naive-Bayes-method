<ul> ► Tetap beresiko terinfeksi kembali </ul>
<ul> ► Pembentukan  imunitas/kekebalan alami tidak  bertahan selamanya</ul>
<ul> ► Waktu  Pembentukan imunitas  bervariasi </ul><br>

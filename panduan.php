<ul  style="color: black;">
Kebiasaan tersebut merupakan langkah awal agar dapat terhindar dari paparan Virus Corona.Namun meskipun telah melakukan hal-hal tersebut  masih memungkinkan kita terpapar oleh Virus Corona dilihat dari tingkat pernyebaran virus yang sangat tinggi terutama dinegara kita,Indonesia. Oleh karena itu tidak ada salahnya jika sesekali kita melakukan test untuk memeriksa diri kita pakah terpapar atau tidak dengan melakukan beberapa test seperti : </ul><br>

<li style="color: black;"> 1. Tes molekuler (disebutjuga tes RNA atau PCR) </li>
<li style="color: black;">Tes ini dianggapp paling akurat untuk mendeteksi adanya virus aktif. Hasil tes juga sangat akurat. 
Metode pengujian dilakukan dengan mengumpulkan sampel lendir dari hidung atau tenggrokan dengan menggunakan kain penyeka khusus. 
Sampel yang dikumpulkan dipakai untuk mendeteksi adanya materi genetik virus. Tes molekuler juga sering disebut test PCR, 
yang merupakan singkatan dari polymerase chain reaction. Hasil tes ini bisa didapatkan dalam hitungan menit hingga hari,
tergantung dari fasilitas laboratorium.</li><br>
<li style="color: black;"> 2. Tes antigen atau rapid test </li>
<li style="color: black;">
    Jenis test ini juga disebut dengan "rapid test" karena hasilnya bisa didapat dalam hitungan menit. Biaya yang dibutuhkan juga lebih murah. Sayangnya, tingkat akurasi utuk mendeteksi infeksi aktif sangat rendah sehingga jenis test ini tidak disarankan untuk mendeteksi Covid-19. Rapid test juga bisa dilakukan di mana saja, tanpa perlu analisis laboratorium.
</li><br>
<li style="color: black;"> 3. Test antibodi atau tes darah </li>
<li style="color: black;">
    Tes ini digunakan untuk mencari antibodi terhadap virus corona. Antibodi adalah protein yang diproduksi oleh sistem kekebalan untuk melawan penyerang asing, seperti virus. Tes antibodi COVID-19 tidak dapat mendiagnosis infeksi virus korona aktif. Jenis tes ini hanya mendeteksi apakah kita pernah mengalami infeksi di masa lalu. Pasalnya, antibodi tidak dapat dideteksi sampai beberapa hari setelah infeksi dimulai. Jenis test ini juga tidak disarankan untuk mendeteksi Covid-19 hingga pasien mengalami gejala minimal dalam waktu 14 hari.
</li><br>

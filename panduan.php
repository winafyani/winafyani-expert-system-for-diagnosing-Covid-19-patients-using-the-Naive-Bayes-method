<p><u>REHABILITASI SESUDAH SEMBUH COVID19 </u></p> <br>
<ul> 1. Panduan olahraga </ul>
<ul> ► Lakukan pemanasan dan pendinginan   </ul>
<ul> ► Kenakan pakaian yang longgar dan nyaman </ul>
<ul> ► Tunggu satu jam setelah makan sebelum berolahraga  Minum banyak air </ul>
<ul> ► Hindari berolahraga dalam cuaca yang sangat panas   </ul>
<ul> ► Olahraga di dalam ruangan bila cuaca sangat dingin </ul><br>
<ul> A. Berhenti olahraga apabila : </ul>
<ul> ► Mual </ul>
<ul> ► Pusing atau nyeri kepala   </ul>
<ul> ► Sesak napas yang parah </ul>  
<ul> ► Keringat berlebih </ul>
<ul> ► Nyeri Dada   </ul>
<ul> ► Nyeri otot hebat </ul><br>
<ul> 2. Mengelola masalah pada suara </ul>
<ul> Beberapa orang dapat mengalami masalah pada suara, terutama  setelah diberi ventilasi (selang pernapasan) saat dirawat. </ul><br>
<ul> A. Jika suara serak atau lemah, perhatikan beberapa hal ini : </ul>
<ul> ► Jangan berbisik karena dapat membuat pita suara Anda tegang.  Cobalah untuk tidak meninggikan suara atau berteriak. </ul>
<ul> ► Istirahat jika kehabisan napas saat berbicara, Berhenti dan  duduk, sambil fokus pada pernapasan Anda. </ul>
<ul> ► Cobalah bernyanyi sendiri untuk berlatih menggunakan suara  anda </ul>
<ul> ► Gunakan cara lain untuk berkomunikasi, seperti menulis,  mengirim pesan, atau menggunakan gerakan, jika sulit atau  tidak nyaman saat berbicara </ul>
<ul> ► Minumlah air sepanjang hari untuk membantu menjaga suara  anda </ul><br>
<ul> 3. Mengelola masalah pada menelan </ul>
<ul> ► Setelah sembuh, beberapa orang dapat mengalami kesulitan dalam menelan  terutama setelah diberi ventilasi (selang pernapasan) saat dirawat. </ul><br>
<ul> A. Jika anda mengalami kesulitan menelan, teknik berikut dapat membantu: </ul>
<ul> ► Duduklah dengan tegak setiap makan atau minum. Jangan makan atau minum sambil berbaring. </ul>
<ul> ► Tetap tegak (duduk, berdiri, berjalan) setidaknya selama 30 menit setelah  makan. </ul>
<ul> ► Cobalah makanan dengan konsistensi berbeda (tebal dan tipis). </ul>
<ul> ► Konsentrasi saat makan atau minum. </ul><br>
<ul> ► Pastikan mulut kosong sebelum menggigit atau mengunyah lagi. </ul><br>
<ul> 4. Mengelola masalah pada perhatian,  memori, dan berpikir jernih </ul>
<ul> Jika Anda mengalami masalah ini, strategi ini dapat membantu: </ul>
<ul> ► Latihan fisik / olahraga dapat membantu otak anda pulih. </ul>
<ul> ► Latihan otak, seperti hobi atau aktivitas baru, teka-teki, permainan  kata dan angka, latihan memori, dan membaca dapat membantu. </ul>
<ul> ► Ingatkan diri anda dengan daftar, catatan, dapat mengingatkan anda  tentang hal-hal yang perlu Anda lakukan. </ul>
<ul> ► Bagi aktivitas menjadi langkah-langkah kecil untuk menghindari  perasaan kewalahan. </ul>
<ul> ► Makan makanan kecil sepanjang hari jika lelah dengan makanan normal , Jika batuk atau tersedak, atau sulit bernafas saat makan dan minum,  istirahatlah untuk memulihkan diri. </ul><br>
<ul> 5. Mengelola stres, kecemasan, atau depresi </ul>
<ul> A. Jaga kebutuhan dasar Anda </ul>
<ul> ► Tidur dengan cukup dan berkualitas.  </ul>
<ul> ► Makan dengan cukup dan sehat  Aktif secara fisik </ul><br>
<ul> B. Perawatan diri / Self Care </ul>
<ul> ►  Berbicara dengan orang lain dapat membantu mengurangi stres </ul>
<ul> ►  Lakukan aktivitas santai yang  tidak membuat terlalu lelah,seperti mendengarkan musik, membaca, atau latihan spiritual. </ul>
<ul> ► Secara bertahap, mulai aktivitas atau hobi semampu anda, hal  ini dapat membantu meningkatkan suasana hati Anda. </ul><br>


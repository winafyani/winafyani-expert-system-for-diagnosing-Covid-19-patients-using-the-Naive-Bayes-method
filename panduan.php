<ul> 2. Setelah dinyatakan sembuh,  mengapa saya masih bergejala ? </ul>
<ul> Fenomena long COVID-19 : Adanya gejala COVID-19 yang bertahan  atau muncul kembali berminggu-minggu hingga berbulan-bulan  setelah pasien dinyatakan sembuh. </ul>
<ul>  ♦ Gejala yang sering tetap  bertahan : </ul>
<ul> - Mudah lelah  Batuk </ul>
<ul> - Hilang penciuman </ul>
<ul> - Nyeri kepala  Nyeri otot  Mual, diare </ul>
<ul> - Nyeri perut dan dada </ul>
<ul>  ♦ Long Covid : </ul>
<ul> - Tidak menular, boleh beraktivitas biasa  Merupakan sisa gejala covid </ul>
<ul> - Tetap rutin kontrol dokter </ul>
<ul> ♦ Faktor resiko mengalami Long Covid: </ul>
<ul> - Darah tinggi </ul>
<ul> - Obesitas </ul>
<ul> - Gangguan jiwa </ul><br>
<ul> 3. Setelah sembuh,apakah organ tubuh saya ada yang  rusak ? </ul>
<ul>  Semua penderita Covid ringan atau berat beresiko  mengalami gangguan organ jangka panjang, meskipun tidak  semua orang akan mengalami hal ini </ul>
<ul>  ♦ Organ yang terdampak : </ul>
<ul> ► Jantung : gagal jantung </ul>
<ul> ► Paru : kerusakan jaringan paru </ul>
<ul> ► Otak dan Saraf : hilang penciuman,  resiko stroke </ul>
<ul> ► Otot : nyeri otot, letih lemah </ul>
<ul> ► Kesehatan mental : gangguan cemas,  sulit tidur </ul><br>
<ul> 4. Setelah sembuh,apakah saya bisa kena covid lagi ? </ul>
<ul> Meski sudah sembuh, masih tetap bisa  terinfeksi kembali, disebut terinfeksi waktu terbentuknya imunitas / kekebalan  alami setelah terkena covid, berbeda pada  setiap orang . Selang waktu terjadinya reinfeksi sangat  bervariasi, mulai dari beberapa minggu  hingga beberapa bulan setelah dinyatakan  sembuh. </ul><br>
<ul> 5. Setelah sembuh,perlukah tetap menerapkan  protokol kesehatan ? </ul>
<ul> Tetap Harus menerapkan  protokol kesehatan, dikarenakan meskipun telah  sembuh, masih bisa  terinfeksi kembali dan  menularkan ke orang lain. </ul><br>
